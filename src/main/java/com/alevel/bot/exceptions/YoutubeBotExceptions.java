package com.alevel.bot.exceptions;

public class YoutubeBotExceptions extends Exception{

    public YoutubeBotExceptions() {
        super();
    }

    public YoutubeBotExceptions(String message) {
        super(message);
    }

    public YoutubeBotExceptions(String message, Throwable cause) {
        super(message, cause);
    }

    public YoutubeBotExceptions(Throwable cause) {
        super(cause);
    }
}
